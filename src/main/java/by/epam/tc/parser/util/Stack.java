package by.epam.tc.parser.util;

public class Stack<E> extends java.util.Stack<E> {
    @Override
    public E push(E item) {
        return super.push(item);
    }

    @Override
    public E pop() {
        return super.pop();
    }

    @Override
    public E peek() {
        return super.peek();
    }
}
